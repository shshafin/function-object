var color ="purple";

if(color=="red"){
    console.log('redddd');
}

else if(color=="blue"){
    console.log('blueeee');
}

else if(color=="purple"){
    console.log('purpleeee');
}

else{
    console.log('nothing');
}

// switch

switch(color){
    case 'blue':
        console.log('blueee');
        break;
    case 'red':
        console.log('redddd');
        break;
    case 'pink':
        console.log('pinkkkk');
        break;
    case 'brown':
        console.log('browneeeee');
        break;
    case 'gray':
        console.log('grayyyyy');
        break;
    case 'white':
        console.log('whiteee');
        break;
    case 'black':
        console.log('blackkk');
        break;
    case 'purple':
        console.log('purpleeee');
        break;

        default:
            console.log('nooooo');

}